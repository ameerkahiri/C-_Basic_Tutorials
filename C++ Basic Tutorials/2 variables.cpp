// #2 Variables

#include <iostream>

using namespace std;

int main() {
	
	//declare an integer
	int age;
	
	//declare a string
	string first_name;
	
	//declare a character
	char grade;
	
	
	
	//assign the integer
	age = 25;
	
	//assign the string
	first_name = "Ameer";
	
	//assign the char
	grade = 'B';
	
	//print all the variable as a string
	cout << "Name : " << first_name << endl << "Grade : " << grade << endl << "Age : " << age << endl;
	
	return 0;
}

